public class CircleArea {

    public static void main(String[] args) {
        final double PI = 3.14; // declare a constant for pi

        double radius = 10.0; // declare a variable for the radius
        double circleArea = radius * radius * PI; // calculate the area of the circle

        // print the area of the circle
        System.out.println("The area of the circle with radius " + radius + " is: " + circleArea);

    }
}